const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const TEXTURES_DIR = path.join(ROOT, "assets", "textures");
const OUT_JS = path.join(ROOT, "src", "textures.generated.js");
const OUT_CSS = path.join(ROOT, "src", "textures.generated.css");

const NAME_MAP = {
  carpet: "alfombra",
  ceramic: "ceramica",
  dirt: "tierra",
  grass: "pasto",
  sand: "arena",
  stone: "piedra",
  water: "agua",
  wood: "madera",
  wood_2: "azulejo",
};

function humanize(id) {
  if (NAME_MAP[id]) return NAME_MAP[id];
  return id
    .split("_")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

const files = fs.readdirSync(TEXTURES_DIR)
  .filter((f) => f.endsWith(".svg"))
  .sort();

const textures = files.map((f) => {
  const id = path.basename(f, ".svg");
  return { id, name: humanize(id) };
});

const js = `// Auto-generated by scripts/generate-textures.js
// Do not edit manually. Run \`node scripts/generate-textures.js\` to update.
window.__TEXTURES_DATA = ${JSON.stringify(textures, null, 2)};
`;

fs.writeFileSync(OUT_JS, js, "utf-8");

function cssRules(selector) {
  return textures
    .filter((t) => fs.existsSync(path.join(TEXTURES_DIR, `${t.id}.svg`)))
    .map((t) => `${selector}.texture-${t.id} {\n  background-image: url("../assets/textures/${t.id}.svg");\n}`)
    .join("\n\n");
}

const css = `/* Auto-generated by scripts/generate-textures.js */
/* Do not edit manually. Run \`node scripts/generate-textures.js\` to update. */

${cssRules(".texture-swatch")}

${cssRules(".cell")}
`;

fs.writeFileSync(OUT_CSS, css, "utf-8");

console.log(`✓ ${textures.length} textures found`);
textures.forEach((t) => console.log(`  ${t.id}.svg → "${t.name}"`));
console.log(`\nWrote ${OUT_JS}`);
console.log(`Wrote ${OUT_CSS}`);
